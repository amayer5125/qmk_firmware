bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case QWERTYHOME:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTYHOME);
      }
      return false;
      break;
  }
  return true;
}
